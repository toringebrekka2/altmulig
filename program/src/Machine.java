public class Machine {

    public Machine(){

    }

    public int plus(int a, int b) {
        return a+b;
    }

    public int minus(int a, int b) {
        return a-b;
    }

    public int times(int a, int b) {
        return a*b;
    }

    public int div(int a, int b) {
        return a/b;
    }
}
